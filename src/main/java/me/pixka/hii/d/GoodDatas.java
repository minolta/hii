package me.pixka.hii.d;

import java.math.BigDecimal;

public class GoodDatas {
	private Long h;

	private Long d;
	private Long m;
	private Long y;
	private BigDecimal amount;

	public Long getD() {
		return d;
	}

	public void setD(Long d) {
		this.d = d;
	}

	public Long getM() {
		return m;
	}

	public void setM(Long m) {
		this.m = m;
	}

	public Long getY() {
		return y;
	}

	public void setY(Long y) {
		this.y = y;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public Long getH() {
		return h;
	}

	public void setH(Long h) {
		this.h = h;
	}

}
